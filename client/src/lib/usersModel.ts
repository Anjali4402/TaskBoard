import mongoose from "mongoose";

const usersSchema = new mongoose.Schema(
  {
    fullName: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true },
);

export const userModelSchema =
  mongoose.models.users || mongoose.model("users", usersSchema);
